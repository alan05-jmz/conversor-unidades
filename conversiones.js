// ===== DISTANCIA =====

function metrosAKilometros(m) {
    return m / 1000;
}

function kilometrosAMetros(km) {
    return km * 1000;
}

function metrosAMillas(m) {
    return m * 0.000621371;
}

function millasAMetros(mi) {
    return mi / 0.000621371;
}

// ===== PESO =====

function gramosAKilogramos(g) {
    return g / 1000;
}

function kilogramosAGramos(kg) {
    return kg * 1000;
}

function gramosALibras(g) {
    return g * 0.00220462;
}

function librasAGramos(lb) {
    return lb / 0.00220462;
}